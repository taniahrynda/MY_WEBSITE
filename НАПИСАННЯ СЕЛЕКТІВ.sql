-- Статистика продажу книжок різних категорій--

SELECT CATEGORY.TYPE_OF_BOOK, CATEGORY.LANGUAGE_BOOK, CATEGORY.FORMAT_BOOK, SUM(ORDERS.AMOUNT) AS TOTAL_REVENUE
FROM CATEGORY
JOIN BOOK ON CATEGORY.ID = BOOK.CATEGORY_ID
JOIN ORDERS ON BOOK.ID = ORDERS.BOOK_ID
GROUP BY CATEGORY.TYPE_OF_BOOK, CATEGORY.LANGUAGE_BOOK, CATEGORY.FORMAT_BOOK;

-- Показати книги, які ніхто не купив--
