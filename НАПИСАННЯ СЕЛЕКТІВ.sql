-- Перегляд наступної інформації про книги: назва, автор, ціна --
SELECT NAME, FIRSTNAME, LASTNAME, PRICE 
FROM BOOK, AUTHOR
WHERE AUTHOR_ID=AUTHOR.ID;

-- Перегляд усіх книг, що належать до різновиду літератири Х --
SELECT BOOK.NAME
FROM BOOK
JOIN CATEGORY ON CATEGORY.ID = BOOK.CATEGORY_ID
WHERE CATEGORY.TYPE_OF_BOOK = 'Художня література';

-- Перегляд книг написаних мовою Х --
SELECT NAME
FROM BOOK, CATEGORY
WHERE CATEGORY_ID=CATEGORY.ID
AND LANGUAGE_BOOK IN ('Німецька');

-- Перегляд книг, що мають формат Х --
SELECT NAME
FROM BOOK, CATEGORY
WHERE CATEGORY_ID=CATEGORY.ID
AND FORMAT_BOOK IN ('Електронна');

-- Список книг, які найчасті купляли клієнти в Х році --

SELECT NAME, COUNT(*) AS COUNT_BOOK
FROM BOOK JOIN ORDERS
ON BOOK_ID=BOOK.ID
JOIN CATEGORY ON CATEGORY_ID=CATEGORY.ID
WHERE YEAR(DATE_ORDER)=2020
GROUP BY NAME
ORDER BY COUNT_BOOK DESC;

-- Cписок усії книг, які купив покупець Х і скільки він заплатив--
SELECT CUSTOMER.FIRSTNAME, CUSTOMER.LASTNAME, BOOK.NAME, SUM(ORDERS.AMOUNT) AS TOTAL_PRICE
FROM CUSTOMER
JOIN ORDERS ON CUSTOMER.ID = ORDERS.CUSTOMER_ID
JOIN BOOK ON BOOK.ID = ORDERS.BOOK_ID
WHERE CUSTOMER.ID = 5
GROUP BY CUSTOMER.FIRSTNAME, CUSTOMER.LASTNAME, BOOK.NAME;

-- Рейтинг категорій--
SELECT category.TYPE_OF_BOOK, SUM(ORDERS.NUMBERS) AS TOTAL_NUMBERS
FROM category
JOIN BOOK ON CATEGORY_ID = category.ID
JOIN ORDERS ON BOOK.ID = ORDERS.BOOK_ID
GROUP BY category.TYPE_OF_BOOK
ORDER BY TOTAL_NUMBERS DESC;




SELECT TYPE_OF_BOOK, LANGUAGE_BOOK, FORMAT_BOOK, SUM(NUMBERS) AS TOTAL_NUMBERS  
FROM BOOK JOIN category
ON CATEGORY_ID=category.ID
JOIN ORDERS ON BOOK.ID=BOOK_ID
GROUP BY TYPE_OF_BOOK, LANGUAGE_BOOK, FORMAT_BOOK
ORDER BY TOTAL_NUMBERS DESC;

-- Список клієнтів, які купляли в магазині більше 5 разів--
SELECT FIRSTNAME, LASTNAME, COUNT(NUMBERS) AS COUNT_NUMBERS
FROM AUTHOR
JOIN BOOK ON AUTHOR_ID = AUTHOR.ID
JOIN ORDERS ON BOOK_ID = BOOK.ID
GROUP BY FIRSTNAME, LASTNAME
HAVING COUNT(NUMBERS) > 5;



